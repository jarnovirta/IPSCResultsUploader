package fi.ipsc_results_uploader;

public class Match {
	private int matchId;
	private String MatchName;
	public int getMatchId() {
		return matchId;
	}
	public void setMatchId(int matchId) {
		this.matchId = matchId;
	}
	public String getMatchName() {
		return MatchName;
	}
	public void setMatchName(String matchName) {
		MatchName = matchName;
	}
	
}
